def get_script_prompt(topic: str, channel: str | None = None) -> str:
    """Возвращает промпт для генерации сценария (TTS + изображения)."""
    ch = channel or CHANNEL_DEFAULT
    return PROMPT_VIDEO_STRUCTURE.format(topic=topic, channel=ch)


def get_tts_script_prompt(topic: str, channel: str | None = None) -> str:
    """Возвращает промпт для генерации текстового сценария (narrate)."""
    ch = channel or CHANNEL_DEFAULT
    return PROMPT_SCRIPT_TTS.format(topic=topic, channel=ch)


def get_image_prompt(description: str) -> str:
    """Возвращает промпт для генерации инфографики по описанию."""
    return PROMPT_IMAGE_INFОGRAPHIC.format(description=description)
