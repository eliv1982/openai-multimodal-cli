"""
Чёткие промпты для каждого типа запроса в проекте.

Используются при генерации сценария для видео и как шаблоны для ручного ввода.
"""


# --- 1. ПРОМПТ ДЛЯ ОЗВУЧКИ ТЕКСТА (СЦЕНАРИЙ ДЛЯ TTS) ---
# Генерирует текст, который будет озвучен диктором.

PROMPT_SCRIPT_TTS = """Напиши сценарий для озвучки образовательного видео на тему «{topic}» продолжительностью 1,5–2 минуты.

Сценарий должен включать:
- Вступление: краткое объяснение сути темы (что это, зачем нужно).
- Основные аспекты: ключевые причины, мотивы, предпосылки.
- Этапы/процесс: как это делается, какие шаги (с примерными сроками, если применимо).
- Плюсы: выгоды, преимущества, сохранение важных атрибутов.
- Риски и сложности: о чём важно знать, возможные подводные камни.
- Заключение: краткий вывод и рекомендация обратиться к специалистам.

Язык — простой, без канцеляризмов, с примерами из жизни. Тон — дружелюбный, уверенный. Текст должен легко читаться диктором."""


# --- 2. ПРОМПТ ДЛЯ ГЕНЕРАЦИИ ИЗОБРАЖЕНИЯ (ИНФОГРАФИКА/ИЛЛЮСТРАЦИЯ) ---
# Передаётся в image_gen при создании иллюстраций к видео или вручную через CLI.

PROMPT_IMAGE_INFОGRAPHIC = """Создай инфографику в формате 16:9, иллюстрирующую тему: {description}.

Требования:
- Плоский стиль (flat design).
- Основные цвета — синий и зелёный, фон светлый.
- Центральный образ: стилизованная иконка или символ темы.
- Стрелки, блоки, подписи для структуры информации.
- Пиктограммы: часы (сроки), щит (защита), деньги (выгоды) — по смыслу.
- Крупная подпись внизу, отражающая суть.
- Изображение должно быть понятным, без мелких деталей, хорошо читаться на экране."""


# --- 3. ПРОМПТ ДЛЯ КОРОТКОГО ВИДЕО (СТРУКТУРА СЦЕН) ---
# Задаёт GPT, как разбить сценарий на части и какие изображения генерировать.

PROMPT_VIDEO_STRUCTURE = """Создай сценарий для обучающего видеоролика на тему «{topic}» длительностью около 90 секунд.

Видео состоит из шести сцен. Верни ТОЛЬКО валидный JSON-массив из шести объектов. У каждого объекта два поля:

1. part_text (русский) — текст для озвучки этой сцены. Язык простой, дружелюбный, без канцеляризмов. Тон уверенный. Текст легко читается диктором. Длина части — примерно 15–25 секунд озвучки.

2. image_prompt (английский) — подробное описание изображения для этой сцены. Требования к описанию:
   - Формат 16:9, flat design.
   - Цвета: синий, зелёный, светлый фон.
   - Укажи конкретные элементы: иконки, стрелки, подписи, пиктограммы.
   - Изображение иллюстрирует содержание part_text.
   - Без мелких деталей, читаемо на экране.

Структура сцен:
- Сцена 1: титульный кадр — крупный заголовок, карта или общий символический образ.
- Сцена 2: суть темы — что это, зачем.
- Сцена 3: основные причины, мотивы.
- Сцена 4: этапы, процесс.
- Сцена 5: плюсы и риски (можно два блока или символ весов).
- Сцена 6: заключение — призыв обратиться к специалистам.

Не добавляй пояснений. Только JSON."""


# --- Системный промпт для GPT (сценарий) ---
PROMPT_SYSTEM_SCRIPT = """Ты помощник по созданию образовательных сценариев. Отвечай только валидным JSON без markdown-разметки."""


# --- Пример промпта для TTS (ручной ввод) ---
PROMPT_TTS_EXAMPLE = """Напиши сценарий для озвучки образовательного видео на тему «Редомициляция компании: что это и зачем нужно?» продолжительностью 1,5–2 минуты.

Сценарий должен включать:
- Вступление: что такое редомициляция (смена «прописки» компании, когда бизнес переезжает в другую страну без ликвидации).
- Причины для редомициляции: налоговые льготы, защита активов, удобство управления, выход на международные рынки.
- Основные этапы: решение акционеров, подготовка документов, регистрация в новой юрисдикции, исключение из старой (привести примерный срок — 2–6 месяцев).
- Плюсы: сохранение истории компании, контрактов, лицензий.
- Риски и сложности: различия в законодательстве, налоговые последствия, возможные блокировки.
- Заключение: необходимость консультации со специалистами.

Язык — простой, без канцеляризмов, с примерами из жизни. Тон — дружелюбный, уверенный. Текст должен легко читаться диктором."""


# --- Пример промпта для изображения (ручной ввод) ---
PROMPT_IMAGE_EXAMPLE = """Создай инфографику в формате 16:9, иллюстрирующую процесс редомициляции компании. Используй плоский стиль (flat design), основные цвета — синий и зелёный, фон светлый.

В центре изобрази стилизованную компанию (здание или иконку). От неё влево ведёт стрелка с подписью «Старая юрисдикция» (например, флаг или контур страны), вправо — стрелка с подписью «Новая юрисдикция». Над стрелками укажи ключевые этапы: «Решение акционеров», «Подача документов», «Регистрация», «Исключение из реестра». Под стрелками добавь пиктограммы: часы (сроки), щит (защита активов), деньги (налоговые преимущества). Внизу крупно подпись: «Редомициляция — переезд бизнеса без закрытия».

Изображение должно быть понятным, без мелких деталей, хорошо читаться на экране."""


# --- Пример промпта для видео (ручной ввод / описание структуры) ---
PROMPT_VIDEO_EXAMPLE = """Создай анимированный видеоролик длительностью 90 секунд на тему редомициляции. Видео должно состоять из шести сцен:

1. (0:00–0:15) Титульный кадр: крупно «Редомициляция: переезд бизнеса» на фоне карты мира с подсвеченными двумя странами.
2. (0:15–0:35) Объяснение сути: появляется здание компании, затем оно «перелетает» из одной страны в другую. Текст на экране: «Компания меняет юридический адрес, но продолжает работать».
3. (0:35–0:50) Причины: иконки налогов, щита, глобуса — плюс короткие подписи.
4. (0:50–1:05) Этапы: последовательно «Собрание акционеров», «Документы», «Новая регистрация», «Старая регистрация закрыта».
5. (1:05–1:20) Плюсы и риски: слева зелёный блок с плюсами, справа оранжевый с рисками, между ними символ весов.
6. (1:20–1:30) Заключительный слайд: «Нужна консультация? Обращайтесь!»

Используй плавные переходы (затухания, перемещения). Стиль — корпоративный minimal flat."""


def get_script_prompt(topic: str) -> str:
    """Возвращает промпт для генерации сценария (TTS + изображения)."""
    return PROMPT_VIDEO_STRUCTURE.format(topic=topic)


def get_image_prompt(description: str) -> str:
    """Возвращает промпт для генерации инфографики по описанию."""
    return PROMPT_IMAGE_INFОGRAPHIC.format(description=description)
